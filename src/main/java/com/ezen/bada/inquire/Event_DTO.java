package com.ezen.bada.inquire;

public class Event_DTO {
	
	int event_num;
	String title, contents,thumbnail,photo,start_day,expire_day;
	
	public Event_DTO() {}

	public int getEvent_num() {
		return event_num;
	}

	public void setEvent_num(int event_num) {
		this.event_num = event_num;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContents() {
		return contents;
	}

	public void setContents(String contents) {
		this.contents = contents;
	}

	public String getThumbnail() {
		return thumbnail;
	}

	public void setThumbnail(String thumbnail) {
		this.thumbnail = thumbnail;
	}

	public String getPhoto() {
		return photo;
	}

	public void setPhoto(String photo) {
		this.photo = photo;
	}

	public String getStart_day() {
		return start_day;
	}

	public void setStart_day(String start_day) {
		this.start_day = start_day;
	}

	public String getExpire_day() {
		return expire_day;
	}

	public void setExpire_day(String expire_day) {
		this.expire_day = expire_day;
	}
	
	
	
	
	

}
